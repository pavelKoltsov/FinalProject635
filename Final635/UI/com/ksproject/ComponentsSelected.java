package com.ksproject;

public class ComponentsSelected 
{
	
	int hardDriveSelect;
	int chipSetSelect;
	int memorySelect;
	
	
	int processorSelect;
	public int getProcessorSelect() 
	{
		return processorSelect;
	}
	public void setProcessorSelect(int processorSelect) 
	{
		this.processorSelect = processorSelect;
	}
	public int getHardDriveSelect() 
	{
		return hardDriveSelect;
	}
	public void setHardDriveSelect(int hardDriveSelect) 
	{
		this.hardDriveSelect = hardDriveSelect;
	}
	public int getChipSetSelect() 
	{
		return chipSetSelect;
	}
	public void setChipSetSelect(int chipSetSelect) 
	{
		this.chipSetSelect = chipSetSelect;
	}
	public int getMemorySelect() 
	{
		return memorySelect;
	}
	public void setMemorySelect(int memorySelect) 
	{
		this.memorySelect = memorySelect;
	}

	
}
